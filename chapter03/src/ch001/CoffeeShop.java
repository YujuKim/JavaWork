package ch001;

public class CoffeeShop {
	private int id;
	private String name;
	private String location;
	private Coffee coffee;
	private Customer customer;
	public CoffeeShop(int id, String name, String location, Coffee coffee, Customer customer) {
		super();
		this.id = id;
		this.name = name;
		this.location = location;
		this.coffee = coffee;
		this.customer = customer;
	}
	
	
	

}
