package ch01;

public class DataTypeTest {

	public static void main(String[] args) {
		// 1. 정수 550을 name1 이름으로 변수에 담아 보세요
		int name1 = 550;
		System.out.println(name1);
		// 2. 정수 22억을 name2 이름으로 변수에 담아 보세요
		long name2 = 2200000000L;
		System.out.println(name2);
		// 3. 문자 A를 name3 이름으로 변수에 담아 보세요
		char name3 = 'A';
		System.out.println(name3);
		// 4. 실수 float 타입으로 0.12345678을 name4 이름으로 변수에 담아 보세요
		float name4 = 0.12345678F;
		System.out.println(name4);
		// 5. 실수 double 타입으로 200.0035을 name5 이름으로 변수에 담아 보세요
		double name5 = 200.0035;
		System.out.println(name5);
		// 6. 논리 자료형 isOk 이름의 변수를 선언해 보세요
		boolean isOk;
		// 7. isOk변수에 값을 false로 초기화 해보세요
		isOk = false;
		System.out.println(isOk);
		// 8. isOk변수에 값을 true로 변경 해보세요
		isOk = true;
		System.out.println(isOk);
		// 9. 변수란 무엇인지 서술해보세요
		System.out.println("변수란 값을 담을 수 있는 메모리의 공간을 말한다.");
		// 10. 기본 데이터 타입의 종류 8가지를 서술 해보세요
		System.out.println("byte,short,int,long,char,float,double,boolean");

	}

}
