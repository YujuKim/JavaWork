package ch01;

// 중괄호의 범위(블록)는 절대적
public class variable {

	public static void main(String[] args) {
		int age; // 변수 선언
		int count;
		
		age=1; // 변수 초기화
		count=100;
		
		System.out.println(age);
		System.out.println("===================");
		System.out.println(count);
		
		age =500;
		count=300;
		
		System.out.println(age);
		System.out.println("===================");
		System.out.println(count);
		
		int myAge = 800; //변수 선언과 동시에 초기화
		System.out.println(myAge); // sysout + ctrl + space 
		System.out.println(9000);
		 
		
	} //end of main. main: 프로그램의 시작점

} // end of class.
